    private void loadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loadActionPerformed
        // TODO add your handling code here:

        JFrame frame = new JFrame();

        JFileChooser chooser = new JFileChooser();

        FileNameExtensionFilter filter = new FileNameExtensionFilter("Archivo XPRESS", "xpress_save");

        chooser.setFileFilter(filter);

        File file = null;

        int select = chooser.showOpenDialog(frame);

        if (select == JFileChooser.APPROVE_OPTION) {

            file = chooser.getSelectedFile();

            if (file != null) {
                try {
                    
                    c.initializateDataAssignation(file.getPath());
                    
                    next.setEnabled(rootPaneCheckingEnabled);
                    
                    show_problem_info();
                    
                } catch (DistanceNotAccessibleException ex) {
                    Logger.getLogger(LoadInfo.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(this, ex.getMessage(), "", 0);
                } catch (WithoutCapacityException ex) {
                    Logger.getLogger(LoadInfo.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Seleccione un archivo con el problema a analizar", "", 2);
            }
        }

    }//GEN-LAST:event_loadActionPerformed

