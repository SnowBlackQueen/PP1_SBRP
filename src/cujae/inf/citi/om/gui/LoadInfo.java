    private void centrate_data_table(JTable table) {

        DefaultTableCellRenderer render = new DefaultTableCellRenderer();

        render.setHorizontalAlignment(0);

        for (int i = 0; i < table.getColumnCount(); i++) {

            TableColumn column = table.getColumnModel().getColumn(i);

            column.setCellRenderer(render);

        }

    }
    
     private void show_passengers_assigned_by_stop() {

        int selection = table1.getSelectedRow();

        if (selection != -1) {

            ((DefaultTableModel) table2.getModel()).setRowCount(0);

            String id = (String) table1.getModel().getValueAt(selection, 0);

            try {

                HashMap<String, ArrayList<String>> values = c.passenger_assigned_by_bus_stop(id);

                Set<String> keys = values.keySet();

                DefaultTableModel model = (DefaultTableModel) table2.getModel();

                for (String s : keys) {

                    Object[] row = {s, /*values.get(s).get(0), values.get(s).get(1)*/"---", "---"};

                    model.addRow(row);

                }

            } catch (ItemNoFoundException ex) {

                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, ex.getMessage(), "", 0);

            }

        } else {
            table2.clearSelection();
        }

    }

